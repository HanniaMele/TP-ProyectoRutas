public class Ciudad {

    private String clave;
    private String nombre;
    private String estado;
    private String pais;
    private String continente;

    public Ciudad(String clave, String nombre, String estado, String pais, String continente) {
        this.clave = clave;
        this.nombre = nombre;
        this.estado = estado;
        this.pais = pais;
        this.continente = continente;
    }
    
}
